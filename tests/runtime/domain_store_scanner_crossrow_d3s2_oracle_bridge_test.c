/*
 * D3-S2 production crossrow bridge (append-only sibling oracle):
 * executes the D3-S2 suffix vectors from domain-scan-crossrow-v1 through
 * real production begin_profiled_d3s2 + d3s2_drive + finalize/abort against
 * scripted Storage Port rows, then compares status, phase, count/binding
 * masks, sticky, adopted, D2 counters, mutation_calls=0, and exact Port
 * trace against the independent oracle fixture.
 *
 * Suffix inventory (not D3-S2 complete):
 *   - 6 Mode21..26 empty-carrier/empty-secondary COMPLETE smoke
 *   - Mode25 CUM total=1 + RECENT C1 + true ANCHOR COMPLETE success
 *   - Mode25 RECENT-without-CUM BIND carrier-ABSENT STORAGE_CORRUPT (note)
 *   - Mode26 ES resume=1 + MANAGEMENT RESUME + true ANCHOR COMPLETE success
 *   - Mode26 MANAGEMENT-without-ES BIND carrier-ABSENT STORAGE_CORRUPT (note)
 *   - Mode24 RC reply_count=1 + RR RECEIPT + true DELIVERY COMPLETE success
 *   - Mode24 RR-without-RC BIND carrier-ABSENT STORAGE_CORRUPT (note)
 *   - Mode23 TX STATE + ANCHOR + EVIDENCE slots 0..L equation COMPLETE success
 *   - Mode23 EVIDENCE-without-STATE BIND carrier-ABSENT STORAGE_CORRUPT (note)
 *   - Mode22 RC app=1 + DLV-owned ATTEMPT + true DELIVERY COMPLETE success
 *   - Mode22 ATTEMPT-without-RC BIND carrier-ABSENT STORAGE_CORRUPT (note)
 *   - Mode21 STATE cum=1 + TX ATT + AII + true ANCHOR COMPLETE success
 *   - Mode21 ATT-without-AII (CP PRESENT) BIND INDEX pair-ABSENT CORRUPT
 *   - P0-A Mode25 two-owner SHA RETRY interleave + dual CUM carrier COMPLETE
 *   - P0-B Mode26 mid-FOCUS budget stop + same-iterator resume (case13 B5/B6)
 *   - P0-C Mode25 BIND exact_get Port IO_ERROR → STORAGE / FAILED / sticky
 *     STORAGE / incomplete BIND mask / exact Port-trace (H3; no fabricated
 *     CORRUPT outcome). JSON note_count is formal reference-model only
 *     (generator/check); this bridge does not count note_terminal_corrupt.
 *   - P0-D Mode22 unexpected ATTEMPT_ID_INDEX PRESENT (BIND pair ABSENT-peer
 *     fail after carrier+primary); Mode22 true-primary DELIVERY ABSENT;
 *     Mode21 primary PVD mismatch; Mode21 INDEX pair subject/raw mismatch
 *     (pair residual only — true-primary raw unconstructible under D1).
 *     Mode21 success BIND_INDEX no-STATE companion re-get is pinned by
 *     d3_peer_get_count + exact Port-trace (existing success vector).
 *   - P1-A ordinary stream under/over + empty-secondary undercount (case4/14)
 *   - P1-D Mode21 profile_mismatch / future_profile evaluator-off (case12)
 *   - P1-D2 Mode21 count-green without BIND set (case10): call checkpoint at
 *     BIND_ATTEMPT with count_complete full / binding_complete 0; test-only
 *     probe_false_terminal_finalize on session/context copy (EXHAUSTED)
 *     expects INVALID_STATE Port0 no mutation; original resumes COMPLETE.
 *   - P1-B1 Mode22 CLEANUP ordinary-skip + Mode23 still-ordinary (case5)
 *   - P1-B2 Mode24/25/26 CLEANUP PRESENT still-ordinary negatives (case5
 *     residual): cleanup_skip=0, phase FAILED, sticky CORRUPT, no CLEANUP
 *     setup get; real production checkpoint + Port-trace.
 *
 * Fixture is generated by tools/domain_scan_crossrow_d3s2_vector_gen.py
 * emit-c-fixture (no production C translation / no manual expected values).
 * Does NOT #include production unit-test .c sources.
 * Does NOT claim full D3-S2 oracle complete.
 * Stage5 D3 remains unbound. D3-S1 94-vector bridge is separate.
 */

