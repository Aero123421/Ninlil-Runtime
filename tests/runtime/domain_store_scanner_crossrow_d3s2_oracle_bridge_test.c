/*
 * Count S2 peer/FOCUS/BIND exact_gets from Port-trace: gets after the first
 * iter_open. Baseline profile gate (17 gets before any iter_open) is excluded
 * without magic subtraction from spy totals — the begin/baseline boundary is
 * the first iter_open in the mapped oracle-equivalent trace.
 */
static uint64_t count_peer_gets_from_trace(
    const char *const *mapped, size_t mapped_count)
{
    uint64_t peer = 0u;
    int after_iter_open = 0;
    size_t i;

    for (i = 0u; i < mapped_count; ++i) {
        if (strcmp(mapped[i], "begin:READ_ONLY") == 0) {
            after_iter_open = 0;
        } else if (strcmp(mapped[i], "iter_open:prefix0") == 0) {
            after_iter_open = 1;
        } else if (after_iter_open != 0 && strcmp(mapped[i], "get") == 0) {
            peer += 1u;
        }
    }
    return peer;
}

