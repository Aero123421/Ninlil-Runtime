static ninlil_storage_status_t status_for_fault(const ninlil_d3s2_fault_t *f)
{
    if (strcmp(f->shape, "unknown") == 0
        || strcmp(f->status, "UNKNOWN_RAW") == 0) {
        return (ninlil_storage_status_t)99u;
    }
    if (strcmp(f->status, "IO_ERROR") == 0) {
        return NINLIL_STORAGE_IO_ERROR;
    }
    if (strcmp(f->status, "BUSY") == 0) {
        return NINLIL_STORAGE_BUSY;
    }
    if (strcmp(f->status, "CORRUPT") == 0) {
        return NINLIL_STORAGE_CORRUPT;
    }
    if (strcmp(f->status, "UNSUPPORTED_SCHEMA") == 0) {
        return NINLIL_STORAGE_UNSUPPORTED_SCHEMA;
    }
    if (strcmp(f->status, "COMMIT_UNKNOWN") == 0) {
        return NINLIL_STORAGE_COMMIT_UNKNOWN;
    }
    if (strcmp(f->status, "NO_SPACE") == 0) {
        return NINLIL_STORAGE_NO_SPACE;
    }
    return NINLIL_STORAGE_OK;
}

static int map_spy_op_to_oracle_name(ninlil_spy_op_t op, const char **out_name)
{
    switch (op) {
    case NINLIL_SPY_OP_BEGIN:
        *out_name = "begin:READ_ONLY";
        return 1;
    case NINLIL_SPY_OP_GET:
        *out_name = "get";
        return 1;
    case NINLIL_SPY_OP_ITER_OPEN:
        *out_name = "iter_open:prefix0";
        return 1;
    case NINLIL_SPY_OP_ITER_NEXT:
        *out_name = "iter_next";
        return 1;
    case NINLIL_SPY_OP_ITER_CLOSE:
        *out_name = "iter_close";
        return 1;
    case NINLIL_SPY_OP_ROLLBACK:
        *out_name = "rollback";
        return 1;
    case NINLIL_SPY_OP_CLOSE:
        *out_name = "close";
        return 1;
    case NINLIL_SPY_OP_OPEN:
        *out_name = "open";
        return 1;
    case NINLIL_SPY_OP_PUT:
        *out_name = "put";
        return 1;
    case NINLIL_SPY_OP_ERASE:
        *out_name = "erase";
        return 1;
    case NINLIL_SPY_OP_CAPACITY:
        *out_name = "capacity";
        return 1;
    case NINLIL_SPY_OP_COMMIT:
        *out_name = "commit";
        return 1;
    default:
        *out_name = "unknown_enum";
        return 0;
    }
}

/*
 * Count S2 peer/FOCUS/BIND exact_gets from Port-trace: gets after the first
 * iter_open. Baseline profile gate (17 gets before any iter_open) is excluded
 * without magic subtraction from spy totals — the begin/baseline boundary is
 * the first iter_open in the mapped oracle-equivalent trace.
 */
static uint64_t count_peer_gets_from_trace(
    const char *const *mapped, size_t mapped_count)
{
    uint64_t peer = 0u;
    int after_iter_open = 0;
    size_t i;

    for (i = 0u; i < mapped_count; ++i) {
        if (strcmp(mapped[i], "begin:READ_ONLY") == 0) {
            after_iter_open = 0;
        } else if (strcmp(mapped[i], "iter_open:prefix0") == 0) {
            after_iter_open = 1;
        } else if (after_iter_open != 0 && strcmp(mapped[i], "get") == 0) {
            peer += 1u;
        }
    }
    return peer;
}

