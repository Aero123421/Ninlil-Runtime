static ninlil_storage_status_t status_for_fault(const ninlil_d3s2_fault_t *f)
{
    if (strcmp(f->shape, "unknown") == 0
        || strcmp(f->status, "UNKNOWN_RAW") == 0) {
        return (ninlil_storage_status_t)99u;
    }
    if (strcmp(f->status, "IO_ERROR") == 0) {
        return NINLIL_STORAGE_IO_ERROR;
    }
    if (strcmp(f->status, "BUSY") == 0) {
        return NINLIL_STORAGE_BUSY;
    }
    if (strcmp(f->status, "CORRUPT") == 0) {
        return NINLIL_STORAGE_CORRUPT;
    }
    if (strcmp(f->status, "UNSUPPORTED_SCHEMA") == 0) {
        return NINLIL_STORAGE_UNSUPPORTED_SCHEMA;
    }
    if (strcmp(f->status, "COMMIT_UNKNOWN") == 0) {
        return NINLIL_STORAGE_COMMIT_UNKNOWN;
    }
    if (strcmp(f->status, "NO_SPACE") == 0) {
        return NINLIL_STORAGE_NO_SPACE;
    }
    return NINLIL_STORAGE_OK;
}

