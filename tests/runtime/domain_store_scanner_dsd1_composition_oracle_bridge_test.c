/*
 * D3-S2 P2-A DSD1 multi-session composition production bridge
 * (docs/17 §18.13.16).
 *
 * Executes the independent sibling oracle
 *   spec/vectors/domain-scan-dsd1-composition-v1.json
 * through real production private APIs:
 *   begin_profiled_d3s1 / advance / finalize   (modes 11/14/17/19)
 *   begin_profiled_d3s2 / d3s2_drive / finalize (modes 22/23/24)
 *
 * Same D1-valid fixture rows for every session; each session is a fresh
 * scanner session + independent baseline + single READ_ONLY txn + DONE
 * cleanup before the next. Dual-bound S1+S2 is never installed. Mode 28
 * is absent. mutation_calls=0. Does not claim D3-S2 overall complete,
 * Stage 5, D4, public Runtime, ESP-IDF, or hardware.
 *
 * Fixture generated by tools/domain_scan_dsd1_composition_vector_gen.py
 * emit-c-fixture (no production C translation).
 */

